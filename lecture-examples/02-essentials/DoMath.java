import java.lang.Math;

class DoMath {
    public static void main(String args[])
	{
		double a = 30;
		double b = 2;
        double c = 12.75;
        double d = Math.PI;

		System.out.println(Math.pow(a, b));
        System.out.println(Math.sqrt(a));
        System.out.println(Math.floor(c));
        System.out.println(Math.cos(d));
	}
}
