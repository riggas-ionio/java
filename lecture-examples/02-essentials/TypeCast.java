public class TypeCast {
    
    public static void main(String[] args){
        int a = 4/2; 
        int b = 5/2; 
        
        int b2 = (int)5.0/2;
        
        double c= 4.0/2.0; 
        double d = 5/2; 

        System.out.println(a);
        System.out.println(b);
        System.out.println(b2);
        System.out.println(c);
        System.out.println(d);
        
        
    }
}